inline uint8_t encodeNibble(uint8_t nibble) {
    return pgm_read_byte(&hamming74_encode[nibble & 0x0F]);
}

inline uint8_t decodeNibble(uint8_t code) {
    return pgm_read_byte(&hamming74_decode[code & 0x7F]);
}

// Simple CRC8 implementation
inline uint8_t crc8(const uint8_t *data, size_t len) {
    uint8_t crc = 0x00;
    while (len--) {
        uint8_t extract = *data++;
        for (uint8_t tempI = 8; tempI; tempI--) {
            uint8_t sum = (crc ^ extract) & 0x01;
            crc >>= 1;
            if (sum) {
                crc ^= 0x8C;
            }
            extract >>= 1;
        }
    }
    return crc;
}

